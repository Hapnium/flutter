/// Represents the different types of feedback that can be submitted.
///
/// This enum defines the various categories or types of feedback that
/// users can provide, such as feedback related to accounts, shops,
/// trips, the app itself, calls, events, clubs, or communities.
enum FeedbackType {
  /// Feedback related to user accounts.
  account,

  /// Feedback related to shops or businesses.
  shop,

  /// Feedback related to trips or travel experiences.
  trip,

  /// Feedback related to the application itself.
  app,

  /// Feedback related to calls or phone interactions.
  call,

  /// Feedback related to events or scheduled activities.
  event,

  /// Feedback related to clubs or social groups.
  club,

  /// Feedback related to the community or general user experience.
  community,
}